package com.example.controlandmonitorlight.model;

public class Static {
    private int totalWatt ;

    public Static(int totalWatt) {
        this.totalWatt = totalWatt;
    }

    public int getTotalWatt() {
        return totalWatt;
    }

    public void setTotalWatt(int totalWatt) {
        this.totalWatt = totalWatt;
    }
}
